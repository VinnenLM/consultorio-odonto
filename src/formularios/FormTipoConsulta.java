    private void jB_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_excluirActionPerformed
        int opcao = jT_tipoConsulta.getSelectedRow();
        if (opcao >= 0) {
            TipoConsulta tipcon = new TipoConsulta();
            tipcon.setId_tipoConsulta(Integer.parseInt(jT_tipoConsulta.getValueAt(opcao, 0).toString()));

            MetTipoConsulta mettipcon = new MetTipoConsulta();
            mettipcon.excluir(tipcon);
            preencherTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um linha!");
        }
    }//GEN-LAST:event_jB_excluirActionPerformed

