            jB_cadastrar.setText("Atualizar");
            jT_id.setText(jT_tipoConsulta.getValueAt(opcao, 0).toString());
            jT_nomeConsulta.setText(jT_tipoConsulta.getValueAt(opcao, 1).toString());
            jT_valor.setText(jT_tipoConsulta.getValueAt(opcao, 2).toString());
