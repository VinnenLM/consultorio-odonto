    public void preencherTabela() {
        MetTipoConsulta mettipcon = new MetTipoConsulta();
        List<TipoConsulta> lista = mettipcon.ListarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_tipoConsulta.getModel();
        modeloTabela.setRowCount(0);
        for (TipoConsulta tipcon : lista) {
            modeloTabela.addRow(new Object[]{tipcon.getId_tipoConsulta(), tipcon.getTipo_consulta(), tipcon.getValor()});
        }
    }

    public void preencherTabela(String listtipcon) {
        MetTipoConsulta mettipcon = new MetTipoConsulta();
        List<TipoConsulta> lista = mettipcon.ListarTipCon(listtipcon);
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_tipoConsulta.getModel();
        modeloTabela.setRowCount(0);
        for (TipoConsulta tipcon : lista) {
            modeloTabela.addRow(new Object[]{tipcon.getId_tipoConsulta(), tipcon.getTipo_consulta(), tipcon.getValor()});
        }
    }

    public void cadastrar() {
        TipoConsulta tipcon = new TipoConsulta();
        tipcon.setTipo_consulta(jT_nomeConsulta.getText());
        tipcon.setValor(jT_valor.getText());

        MetTipoConsulta mettipcon = new MetTipoConsulta();

        if (jT_nomeConsulta.getText().isEmpty() || jT_valor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            mettipcon.cadastrar(tipcon);
            preencherTabela();

            jT_id.setText(null);
            jT_nomeConsulta.setText(null);
            jT_valor.setText(null);
            jTabbedPane1.setSelectedIndex(1);
        }
    }

    public void editar() {
        TipoConsulta tipcon = new TipoConsulta();
        tipcon.setId_tipoConsulta(Integer.parseInt(jT_id.getText()));
        tipcon.setTipo_consulta(jT_nomeConsulta.getText());
        tipcon.setValor(jT_valor.getText());
        MetTipoConsulta metf = new MetTipoConsulta();

        if (jT_nomeConsulta.getText().isEmpty() || jT_valor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            metf.atualizar(tipcon);
            preencherTabela();

            jTabbedPane1.setSelectedIndex(1);

            jT_id.setText(null);
            jT_nomeConsulta.setText(null);
            jT_valor.setText(null);
            jB_cadastrar.setText("Cadastrar");
            cadAux = 0;
        }
    }

