    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int opcao = jT_prontuario.getSelectedRow();
        if (opcao >= 0) {
            Prontuario pro = new Prontuario();
            pro.setId_prontuario(Integer.parseInt(jT_prontuario.getValueAt(opcao, 0).toString()));
            MetProntuario metpro = new MetProntuario();
            metpro.excluir(pro);
            preencherTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um linha!");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

