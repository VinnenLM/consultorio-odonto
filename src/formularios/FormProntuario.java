    public void preencherTabela() {
        MetProntuario metrec = new MetProntuario();
        List<Prontuario> lista = metrec.ListarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_prontuario.getModel();
        modeloTabela.setRowCount(0);
        for (Prontuario pro : lista) {
            modeloTabela.addRow(new Object[]{pro.getId_prontuario(), pro.getData(), pro.getInicio(), pro.getFim(), pro.getResultado(), pro.getObservacao(), pro.getPac().getNome(),  pro.getMedc().getNome()});
        }
    }

    public void preencherNome(String nome) {
        MetProntuario metrec = new MetProntuario();
        List<Prontuario> lista = metrec.ListarNome(nome);
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_prontuario.getModel();
        modeloTabela.setRowCount(0);
        for (Prontuario pro : lista) {
            modeloTabela.addRow(new Object[]{pro.getId_prontuario(), pro.getData(), pro.getInicio(), pro.getFim(), pro.getResultado(), pro.getObservacao(), pro.getPac().getNome(), pro.getMedc().getNome()});
        }
    }

    public List GetDados() {
        List lista = new ArrayList();
        RelProntuario print = new RelProntuario();
        print.setData(jT_data.getText());
        print.setInicio(jF_inicio.getText());
        print.setFim(jF_fim.getText());
        print.setResultado(jT_resultado.getText());
        print.setObservacao(jT_observacao.getText());
        print.setPaciente(jT_paciente.getText());
        print.setMedicamento(jT_medicamento.getText());
        lista.add(print);

        return lista;
    }

    public List GetDadosTab() {

        int opcao = jT_prontuario.getSelectedRow();
        List lista = new ArrayList();
        RelProntuario print = new RelProntuario();

        if (opcao >= 0) {

            print.setData(jT_prontuario.getValueAt(opcao, 1).toString());
            print.setInicio(jT_prontuario.getValueAt(opcao, 2).toString());
            print.setFim(jT_prontuario.getValueAt(opcao, 3).toString());
            print.setResultado(jT_prontuario.getValueAt(opcao, 4).toString());
            print.setObservacao(jT_prontuario.getValueAt(opcao, 5).toString());
            print.setPaciente(jT_prontuario.getValueAt(opcao, 6).toString());
            print.setMedicamento(jT_prontuario.getValueAt(opcao, 7).toString());
            lista.add(print);
            return lista;
        }
        return null;
    }

