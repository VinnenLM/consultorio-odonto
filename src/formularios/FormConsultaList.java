    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                String sql = "SELECT\n" +
"     *,\n" +
"     paciente.\"pac_id\" AS paciente_pac_id,\n" +
"     paciente.\"pac_nome\" AS paciente_pac_nome,\n" +
"     medico.\"med_id\" AS medico_med_id,\n" +
"     medico.\"med_nome\" AS medico_med_nome,\n" +
"     tipoconsulta.\"tipcon_id\" AS tipoconsulta_tipcon_id,\n" +
"     tipoconsulta.\"tipcon_tipo\" AS tipoconsulta_tipcon_tipo,\n" +
"     tipoconsulta.\"tipcon_valor\" AS tipoconsulta_tipcon_valor,\n" +
"     consulta.\"cons_id\" AS consulta_cons_id,\n" +
"     consulta.\"cons_data\" AS consulta_cons_data,\n" +
"     consulta.\"cons_horario\" AS consulta_cons_horario,\n" +
"     consulta.\"cons_status\" AS consulta_cons_status\n" +
"FROM\n" +
"     \"paciente\" paciente INNER JOIN \"consulta\" consulta ON paciente.\"pac_id\" = consulta.\"pac_id\"\n" +
"     INNER JOIN \"medico\" medico ON consulta.\"med_id\" = medico.\"med_id\"\n" +
"     INNER JOIN \"tipoconsulta\" tipoconsulta ON consulta.\"tipcon_id\" = tipoconsulta.\"tipcon_id\"";
        try{
            Map param = new HashMap();
            Connection con = ConexaoPSQL.dbConnection();
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet resultado = pst.executeQuery();
            JRResultSetDataSource relatConsult = new JRResultSetDataSource(resultado);
            JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\Vinnen\\Documents\\NetBeansProjects\\ProjetoConsultorioPostgreSQL\\src\\relatorios\\relatorio_consulta.jrxml");
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            JasperPrint jpPrint = JasperFillManager.fillReport(jreport, param, relatConsult);
            JasperViewer jv = new JasperViewer(jpPrint,false);
            jv.setVisible(true);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Erro ao imprimir!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

