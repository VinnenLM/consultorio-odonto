    public void preencherTabela() {
        MetConsulta metc = new MetConsulta();
        List<Consulta> lista = metc.ListarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_consulta.getModel();
        modeloTabela.setRowCount(0);
        for (Consulta c : lista) {
            modeloTabela.addRow(new Object[]{c.getId_consulta(), c.getData(), c.getHora(), c.getStatus(), c.getPac().getId_paciente(), c.getPac().getNome(), c.getMed().getId_medico(), c.getMed().getNome(), c.getTipcon().getId_tipoConsulta(), c.getTipcon().getTipo_consulta(), c.getTipcon().getValor()});
        }
    }
    
    public void preencherTabela(String data) {
        MetConsulta metc = new MetConsulta();
        List<Consulta> lista = metc.ListarData(data);
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_consulta.getModel();
        modeloTabela.setRowCount(0);
        for (Consulta c : lista) {
            modeloTabela.addRow(new Object[]{c.getId_consulta(), c.getData(), c.getHora(), c.getStatus(), c.getPac().getId_paciente(), c.getPac().getNome(), c.getMed().getId_medico(), c.getMed().getNome(), c.getTipcon().getId_tipoConsulta(), c.getTipcon().getTipo_consulta(), c.getTipcon().getValor()});
        }
    }
    
