    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int opcao = jT_consulta.getSelectedRow();
        if (opcao >= 0) {
            Consulta c = new Consulta();
            c.setId_consulta(Integer.parseInt(jT_consulta.getValueAt(opcao, 0).toString()));
            c.setData(jT_consulta.getValueAt(opcao, 1).toString());
            c.setHora(jT_consulta.getValueAt(opcao, 2).toString());
            c.setStatus(jT_consulta.getValueAt(opcao, 3).toString());
            
            Paciente p = new Paciente();
            p.setId_paciente(Integer.parseInt(jT_consulta.getValueAt(opcao,4).toString()));
            p.setNome(jT_consulta.getValueAt(opcao, 5).toString());
            c.setPac(p);
            
            Medico m = new Medico();
            m.setId_medico(Integer.parseInt(jT_consulta.getValueAt(opcao, 6).toString()));
            m.setNome(jT_consulta.getValueAt(opcao, 7).toString());
            c.setMed(m);
             
            TipoConsulta tp = new TipoConsulta();
            tp.setId_tipoConsulta(Integer.parseInt(jT_consulta.getValueAt(opcao, 8).toString()));
            tp.setTipo_consulta(jT_consulta.getValueAt(opcao, 9).toString());
            tp.setValor(jT_consulta.getValueAt(opcao, 10).toString());
            c.setTipcon(tp);
            
            FormConsulta fc = new FormConsulta(c);
            fc.setVisible(true);    
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um linha!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

