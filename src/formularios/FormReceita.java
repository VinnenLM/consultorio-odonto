    public void preencherTabela() {
        MetReceita metrec = new MetReceita();
        List<Receita> lista = metrec.ListarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_receita.getModel();
        modeloTabela.setRowCount(0);
        for (Receita r : lista) {
            modeloTabela.addRow(new Object[]{r.getId_receita(), r.getData(), r.getObservacao(), r.getMedc().getNome(), r.getPac().getNome(), r.getMed().getNome()});
        }
    }

    public void preencherNome(String nome) {
        MetReceita metrec = new MetReceita();
        List<Receita> lista = metrec.ListarNome(nome);
        DefaultTableModel modeloTabela = (DefaultTableModel) jT_receita.getModel();
        modeloTabela.setRowCount(0);
        for (Receita r : lista) {
            modeloTabela.addRow(new Object[]{r.getId_receita(), r.getData(), r.getObservacao(), r.getMedc().getNome(),r.getPac().getNome(), r.getMed().getNome()});
        }
    }

    public List GetDados() {
        List lista = new ArrayList();
        RelReceita print = new RelReceita();
        print.setData(jT_data.getText());
        print.setObservacao(jT_observacao.getText());
        print.setMedicamento(jT_medicamento.getText());
        print.setPaciente(jT_paciente.getText());
        print.setMedico(jT_medico.getText());
        lista.add(print);

        return lista;
    }

    public List GetDadosTab() {

        int opcao = jT_receita.getSelectedRow();
        List lista = new ArrayList();
        RelReceita print = new RelReceita();

        if (opcao >= 0) {

            print.setData(jT_receita.getValueAt(opcao, 1).toString());
            print.setObservacao(jT_receita.getValueAt(opcao, 2).toString());
            print.setMedicamento(jT_receita.getValueAt(opcao, 3).toString());
            print.setPaciente(jT_receita.getValueAt(opcao, 4).toString());
            print.setMedico(jT_receita.getValueAt(opcao, 5).toString());
            lista.add(print);
            return lista;
        }
        return null;
    }

