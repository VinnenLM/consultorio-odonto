    private void jB_gerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_gerarActionPerformed
        Receita r = new Receita();
        r.setData(jT_data.getText());
        r.setObservacao(jT_observacao.getText());
        
        Medicamento medc = new Medicamento();
        medc.setId_medicamento(Integer.parseInt(jT_idMedicamento.getText()));
        r.setMedc(medc);
        
        Paciente p = new Paciente();
        p.setId_paciente(Integer.parseInt(jT_idPaciente.getText()));
        r.setPac(p);
                
        Medico m = new Medico();
        m.setId_medico(Integer.parseInt(jT_idMedico.getText()));
        r.setMed(m);
        
        MetReceita metrec = new MetReceita();
        metrec.cadastrar(r);
        
        ImprimirReceita imprime = new ImprimirReceita();
        List listaDados = GetDados();
        imprime.ImprimirRelatorio(listaDados);
        
        preencherTabela();
        
        jT_data.setText(null);
        jT_idPaciente.setText(null);
        jT_paciente.setText(null);
        jT_idMedico.setText(null);
        jT_medico.setText(null);
        jT_idMedicamento.setText(null);
        jT_medicamento.setText(null);
        jT_observacao.setText(null);
        
        jTabbedPane1.setSelectedIndex(1);
    }//GEN-LAST:event_jB_gerarActionPerformed

